	<h4> Dota 2 Update - November 7th, 2016</h6>
	<ul>
		<li>Reports and Commends can now only be sent in the post-game screen.</li>
		<li>Fixed a problem with the attack-move command which sometimes caused more distant units to be targeted</li>
	</ul>
	<h4>Dota 2 Update - November 4th, 2016</h6>
	<ul>
		<li>You can no longer random in the last 10 seconds in Unranked or in the final pick per team in Ranked</li>
		<li>Repicking in Unranked now behaves the same as Ranked, giving you a new random hero rather than letting you pick a hero directly</li>
		<li>You will no longer have a chance of receiving the same hero after re-randoming</li>
		<li>Increased report sensitivity (making it more likely that a reported user will go into low priority)</li>
	</ul>
	<h4>Dota 2 Update - October 27th, 2016</h6>
